function [mu,eta,w] = S_2D(N)
% this function returns the directional cosines and weights
% for use in the two dimensional discrete ordinates code.
% All angles and weights are from 
%  Lewis and Miller, Computational Methods of Neutron Transport


% note:  N = "44" corresponds to the PARTISN N=4 case, which differs from
% the typical level symmetric LQ values in L&M table 4-1.

% in all cases the order is
%    -mu, -eta
%     mu, -eta
%    -mu,  eta
%     mu,  eta
% moreover, the NN N(N+2)/2 angle pairs (e.g N=4 --> 12 pairs) are 
% arranged such the pair (1+i) is a sign flip of pair (N-i).

if N == 2
    
    mus = [ -0.5773503  -0.5773503
             0.5773503  -0.5773503
            -0.5773503   0.5773503
             0.5773503   0.5773503 ];
    w(1:4) = 1; 
    
elseif N == 4
    
    mus = [-0.8688903 -0.3500212   % 1
           -0.3500212 -0.8688903   % 2
           -0.3500212 -0.3500212   % 3
            0.8688903 -0.3500212   % 4
            0.3500212 -0.8688903   % 5
            0.3500212 -0.3500212   % 6
           -0.3500212  0.3500212   % 7
           -0.3500212  0.8688903   % 8
           -0.8688903  0.3500212   % 9    
            0.3500212  0.3500212   %10
            0.3500212  0.8688903   %11
            0.8688903  0.3500212]; %12 
    w(1:12) = 1/3;
    
elseif N == 44 % "old twodant" set for benchmarking
    
    mus = [-0.90444905 -0.30163878   % 1
           -0.30163878 -0.90444905   % 2
           -0.30163878 -0.30163878   % 3
            0.90444905 -0.30163878   % 4
            0.30163878 -0.90444905   % 5
            0.30163878 -0.30163878   % 6
           -0.30163878  0.30163878   % 7
           -0.30163878  0.90444905   % 8
           -0.90444905  0.30163878   % 9
            0.30163878  0.30163878   %10
            0.30163878  0.90444905   %11
            0.90444905  0.30163878]; %12
    w(1:12) = 1/3;
    
elseif N == 6
    mus = [-0.9261808 -0.2666355 % 1
           -0.6815076 -0.6815076 % 2
           -0.6815076 -0.2666355 % 2
           -0.2666355 -0.9261808 % 1
           -0.2666355 -0.6815076 % 2
           -0.2666355 -0.2666355 % 1
            0.9261808 -0.2666355 % 1
            0.6815076 -0.6815076 % 2
            0.6815076 -0.2666355 % 1
            0.2666355 -0.9261808 % 1
            0.2666355 -0.6815076 % 2
            0.2666355 -0.2666355 % 1
           -0.2666355  0.2666355 % 
           -0.2666355  0.6815076 % 
           -0.2666355  0.9261808 %
           -0.6815076  0.2666355
           -0.6815076  0.6815076  
           -0.9261808  0.2666355
            0.2666355  0.2666355
            0.2666355  0.6815076
            0.2666355  0.9261808
            0.6815076  0.2666355
            0.6815076  0.6815076  
            0.9261808  0.2666355 ];
    w1 =  0.1761263; w2 = 0.1572071;
    w(1:12) = [w1 w2 w2 w1 w2 w1 w1 w2 w1 w1 w2 w1];
    w(13:24) = w(12:-1:1);
 
elseif N == 8
    
elseif N == 12
    
elseif N == 16
    
else
    disp(['no data for N = ',num2str(N)])
    mus=0; w=0;
end
    
mu=mus(:,1); eta=mus(:,2);

end